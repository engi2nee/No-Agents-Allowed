#[test]
fn snapshot_all_rendered_outputs() {
    let tmp = tempfile::tempdir().unwrap();
    let root = tmp.path();
    fs::write(
        root.join(".noagents"),
        ".env\n!.env.example\n*.pem\nsecrets/\n/anchored/dir/\n",
    )
    .unwrap();
    noagents(root).arg("generate").assert().success();

    let mut out = String::new();
    let mut files: Vec<_> = walk(root)
        .into_iter()
        .filter(|p| p.file_name().unwrap() != ".noagents")
        .collect();
    files.sort();
    for f in &files {
        let rel = f.strip_prefix(root).unwrap();
        out.push_str(&format!("===== {} =====\n", rel.display()));
        out.push_str(&fs::read_to_string(f).unwrap());
        out.push('\n');
    }
    insta::assert_snapshot!("all_targets", out);
}

fn walk(dir: &Path) -> Vec<std::path::PathBuf> {
    let mut out = Vec::new();
    for entry in fs::read_dir(dir).unwrap() {
        let path = entry.unwrap().path();
        if path.is_dir() {
            out.extend(walk(&path));
        } else {
            out.push(path);
        }
    }
    out
}
